package com.njwb.pojo;

import java.util.Date;

public class Exchange {
	private int exchangeId;
	private int exchangeProvId;
	private int exchangeStatus;
	private int exchangeCharge;
	private Date createTime;
	private Date updateTime;
	public Exchange() {
		super();
	}
	public Exchange(int exchangeId, int exchangeProvId, int exchangeStatus, int exchangeCharge, Date createTime, Date updateTime) {
		super();
		this.exchangeId = exchangeId;
		this.exchangeProvId = exchangeProvId;
		this.exchangeStatus = exchangeStatus;
		this.exchangeCharge = exchangeCharge;
		this.createTime = createTime;
		this.updateTime = updateTime;
	}
	public int getExchangeId() {
		return exchangeId;
	}
	public void setExchangeId(int exchangeId) {
		this.exchangeId = exchangeId;
	}
	public int getExchangeProvId() {
		return exchangeProvId;
	}
	public void setExchangeProvId(int exchangeProvId) {
		this.exchangeProvId = exchangeProvId;
	}
	public int getExchangeStatus() {
		return exchangeStatus;
	}
	public void setExchangeStatus(int exchangeStatus) {
		this.exchangeStatus = exchangeStatus;
	}
	public int getExchangeCharge() {
		return exchangeCharge;
	}
	public void setExchangeCharge(int exchangeCharge) {
		this.exchangeCharge = exchangeCharge;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "Exchange [createTime=" + createTime + ", exchangeCharge=" + exchangeCharge + ", exchangeId=" + exchangeId + ", exchangeProvId=" + exchangeProvId + ", exchangeStatus=" + exchangeStatus
				+ ", updateTime=" + updateTime + "]";
	}
}
