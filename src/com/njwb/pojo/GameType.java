package com.njwb.pojo;

import java.util.Date;

public class GameType {
	private int typeId;
	private String typeName;
	private int typeStatus;
	private String typePicture;
	private Date createTime;
	private Date updateTime;
	public GameType() {
		super();
	}
	public GameType(int typeId, String typeName, int typeStatus, String typePicture, Date createTime, Date updateTime) {
		super();
		this.typeId = typeId;
		this.typeName = typeName;
		this.typeStatus = typeStatus;
		this.typePicture = typePicture;
		this.createTime = createTime;
		this.updateTime = updateTime;
	}
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public int getTypeStatus() {
		return typeStatus;
	}
	public void setTypeStatus(int typeStatus) {
		this.typeStatus = typeStatus;
	}
	public String getTypePicture() {
		return typePicture;
	}
	public void setTypePicture(String typePicture) {
		this.typePicture = typePicture;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "GameType [createTime=" + createTime + ", typeId=" + typeId + ", typeName=" + typeName + ", typePicture=" + typePicture + ", typeStatus=" + typeStatus + ", updateTime=" + updateTime
				+ "]";
	}
}
